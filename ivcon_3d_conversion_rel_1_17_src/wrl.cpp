bool IVCONV::wrl_write ( FILE *fileout )

/******************************************************************************/
/*
Purpose:
  WRL_WRITE writes graphics data to a WRL file.

    Author: John Burkardt
    Modified: 23 May 1999
    Modified: 14 Mar 2007 Tomasz Lis adjusted to new core and extended
*/
{
	stats.text_num = 0;
	
	fprintf ( fileout, "#VRML %s %s\n","V2.0","utf8");
	fprintf ( fileout, "\n" );
	fprintf ( fileout, "  WorldInfo {\n" );
	fprintf ( fileout, "    title \"%s\"\n", fileout_name );
	fprintf ( fileout, "    info \"WRL file generated by IVREAD.\"\n" );
	fprintf ( fileout, "    info \"Original data in %s\"\n", filein_name );
	fprintf ( fileout, "  }\n" );
	fprintf ( fileout, "\n" );
	stats.text_num+=8;
    if (debug)
	  printf ( "WRL_WRITE: WorldInfo block end at line %lu\n", stats.text_num );

	fprintf ( fileout, "  Viewpoint {\n" );
	fprintf ( fileout, "    description \"%s\"\n", "Initial view" );
	fprintf ( fileout, "    position %f %f %f\n",0.0,0.0,9.0 );
	fprintf ( fileout, "  }\n" );
	fprintf ( fileout, "\n" );
	stats.text_num+=5;
    if (debug)
	  printf ( "WRL_WRITE: Viewpoint block end at line %lu\n", stats.text_num );


	fprintf ( fileout, "  Group {\n" );
	fprintf ( fileout, "    children [\n" );
	stats.text_num+=2;

    unsigned long i;
    for (i=0;i<mesh_num;i++)
      wrl_write_mesh ( fileout,i );

    wrl_write_shapes ( fileout );

	//  End of children
	  fprintf ( fileout, "    ]\n" );
	//  End of Group
	  fprintf ( fileout, "  }\n" );
	  stats.text_num+=2;

	  // Report.
	  printf ( "\n" );
	  printf ( "WRL_WRITE - Wrote %lu text lines.\n", stats.text_num );
	  
    return true;
}
