bool IVCONV::wrl_write_lineset ( FILE *fileout )

/******************************************************************************/
/*
Purpose:
  WRL_WRITE_LINESET writes shape InexedLineSet data to a WRL file.

    Author: John Burkardt
    Modified: 23 May 1999
    Modified: 14 Mar 2007 Tomasz Lis created from WRL_WRITE
*/
{
    fprintf ( fileout, "        geometry IndexedLineSet {\n" );
    stats.text_num++;
    array<unsigned long> shape_cor3_base;
    {
		// IndexedLineSet coord
		fprintf ( fileout, "          coord Coordinate {\n" );
		fprintf ( fileout, "            point [\n" );
		stats.text_num += 2;
        unsigned long shape_idx;
        unsigned long cor3_count=0;
		for ( shape_idx = 0; shape_idx < shape_num; shape_idx++ )
        {
            shape_cor3_base[shape_idx]=cor3_count;
            unsigned long shp_order=shape[shape_idx].order;
            unsigned long icor3;
            for ( icor3 = 0; icor3 < shp_order; icor3++ )
            {
              vec3 vcor3=vec3(shape[shape_idx].cor3[icor3]);
              fprintf ( fileout, "              %f %f %f\n",vcor3[0],vcor3[1],vcor3[2] );
              stats.text_num++;
              cor3_count++;
            }
        }
		fprintf ( fileout, "            ]\n" );
		fprintf ( fileout, "          }\n" );
		stats.text_num += 2;
    }
    { // IndexedLineSet coordIndex.
        fprintf ( fileout, "          coordIndex [\n" );
        stats.text_num++;
        unsigned long shape_idx;
		for ( shape_idx = 0; shape_idx < shape_num; shape_idx++ )
        {
            fprintf ( fileout, "            " );
            unsigned long shp_order=shape[shape_idx].order;
            unsigned long icor3;
            for ( icor3 = 0; icor3 < shp_order; icor3++ )
			    fprintf ( fileout, "%lu ", shape_cor3_base[shape_idx] );
            fprintf ( fileout, "%d \n",-1);
            stats.text_num++;
        }
		fprintf ( fileout, "          ]\n" );
		stats.text_num++;
    }
    { // Colors
		fprintf ( fileout, "          color Color {\n" );
		fprintf ( fileout, "            color [\n" );
		stats.text_num += 2;
        unsigned long j;
		for ( j = 0; j < shape_num; ++j )
        {
            vec3 colr=vec3(shape[j].rgb);
			fprintf ( fileout, "              %f %f %f,\n", colr[0],colr[1],colr[2]);
			stats.text_num++;
		}
		fprintf ( fileout, "            ]\n" );
		fprintf ( fileout, "          }\n" );
		stats.text_num += 2;
    }
    //colorPerVertex=TRUE means that we have no colorIndex block,
    // just one color entry per vertex
    //colorPerVertex=FALSE means we have colorIndex block with
    // per-face colors definition
    fprintf ( fileout, "          colorPerVertex %s\n","FALSE" );
    stats.text_num ++;
    { // IndexedLineset colorIndex
		fprintf ( fileout, "          colorIndex [\n" );
		stats.text_num ++;
        fprintf ( fileout, "            " );
        unsigned long shape_idx;
		for ( shape_idx = 0; shape_idx < shape_num; shape_idx++ )
           	  fprintf ( fileout, "%lu ", shape_idx );
        fprintf ( fileout, "\n" );
		stats.text_num++;
		fprintf ( fileout, "          ]\n" );
        stats.text_num++;
    }
    fprintf ( fileout, "        }\n" );
    stats.text_num++;
    return true;
}
